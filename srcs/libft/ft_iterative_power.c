#include "libft.h"

int	ft_iterative_power(int nb, int power)
{
	int temp;
	int i;

	i = 0;
	temp = 1;
	if (power < 0)
	{
		return (0);
	}
	if (power == 0)
	{
		return (1);
	}
	while (i < power)
	{
		temp = temp * nb;
		i++;
	}
	return (temp);
}
