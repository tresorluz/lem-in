#include "libft.h"
#include <stdlib.h>

char	*ft_getz(char *num)
{
	if ((num = (char *)malloc(sizeof(char) * (2))))
	{
		num[0] = '0';
		num[1] = '\0';
		return (num);
	}
	else
		return (NULL);
}
