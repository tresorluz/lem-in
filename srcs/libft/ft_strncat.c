#include "libft.h"

char	*ft_strncat(char *dest, const char *src, size_t n)
{
	char	*i;

	i = dest;
	while (*i != '\0')
	{
		i++;
	}
	while (*src != '\0' && n > 0)
	{
		*i = *src;
		i++;
		src++;
		n--;
	}
	*i = '\0';
	return (dest);
}
